#include "libft.h"

int	ft_atoi(char const *s)
{
	int				i;
	int				neg;
	unsigned int	nb;

	i = 0;
	neg = 1;
	nb = 0;
	while (s[i] <= 32 && s[i] != 27 && s[i] > 0)
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			neg = -1;
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		nb = nb * 10 + s[i] - 48;
		i++;
	}
	return (neg * nb);
}
